    private void tabelDataMahasiswaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMahasiswaMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount()==1) {
            fieldNim.setText(String.valueOf(tabelDataMahasiswa.getValueAt(tabelDataMahasiswa.getSelectedRow(),0)));
            fieldNama.setText(String.valueOf(tabelDataMahasiswa.getValueAt(tabelDataMahasiswa.getSelectedRow(),1)));
            tombolHapus.setEnabled(true);
            tombolTambah.setText("Ubah");
        }
    }//GEN-LAST:event_tabelDataMahasiswaMousePressed

    private void fieldNamaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldNamaKeyReleased
        // TODO add your handling code here:
        List<Mahasiswa> temp = new ArrayList<>();
        
        for (int i = 0; i < daftarMahasiswa.size(); i++) {
            if (daftarMahasiswa.get(i).getNama().toLowerCase().startsWith(fieldNama.getText().toLowerCase())) {
                temp.add(daftarMahasiswa.get(i));
            }
        }
        MahasiswaTableModel model = new MahasiswaTableModel(temp);
        tabelDataMahasiswa.setModel(model);
    }//GEN-LAST:event_fieldNamaKeyReleased

